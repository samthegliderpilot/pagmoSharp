using System.Reflection;
using System.Text;
using System.Xml.Linq;

if (args.Length < 2)
{
    Console.Error.WriteLine("Usage: ApiDocGen <assemblyPath> <outputPath> [--verify]");
    return 2;
}

var assemblyPath = Path.GetFullPath(args[0]);
var outputPath = Path.GetFullPath(args[1]);
var verify = args.Skip(2).Any(a => string.Equals(a, "--verify", StringComparison.OrdinalIgnoreCase));

if (!File.Exists(assemblyPath))
{
    Console.Error.WriteLine($"Assembly not found: {assemblyPath}");
    return 3;
}

var assembly = Assembly.LoadFrom(assemblyPath);
var xmlPath = Path.ChangeExtension(assemblyPath, ".xml");
var xmlDocs = XmlDocs.Load(xmlPath);
var types = assembly.GetExportedTypes()
    .Where(t => t.Namespace == "pagmo")
    .OrderBy(t => t.FullName, StringComparer.Ordinal)
    .ToArray();

var lines = new List<string>
{
    "# API Reference (Generated)",
    "",
    "This document enumerates the public API visible to consumers of pagmoSharp.",
    "Each listed symbol includes a description from XML docs when available,",
    "with generated fallback descriptions for symbols emitted from autogenerated wrappers.",
    "",
    $"- Generated from assembly: {assemblyPath}",
    $"- XML docs source: {(File.Exists(xmlPath) ? xmlPath : "not found (fallback descriptions used)")}",
    "- Upstream pagmo C++ API index: https://esa.github.io/pagmo2/docs/cpp/cpp_docs.html",
    $"- Generation timestamp (UTC): {DateTime.UtcNow:u}",
    ""
};

const BindingFlags declared = BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance | BindingFlags.Static | BindingFlags.DeclaredOnly;

foreach (var type in types)
{
    var typeKind = type.IsInterface ? "interface" : type.IsEnum ? "enum" : type.IsValueType ? "struct" : "class";
    lines.Add($"## {type.FullName}");
    lines.Add("");
    lines.Add($"- Kind: {typeKind}");
    lines.Add($"- Summary: {xmlDocs.GetOrDefault(DocId.ForType(type), $"Represents `{type.FullName}` in the managed pagmo API surface.")}");
    lines.Add($"- Upstream reference: {GetPagmoReference(type.Name)}");
    lines.Add("");

    if (type.IsEnum)
    {
        lines.Add("### Values");
        lines.Add("");
        foreach (var name in Enum.GetNames(type))
        {
            lines.Add($"- {name}");
        }
        lines.Add("");
        continue;
    }

    var ctors = type.GetConstructors(declared)
        .Where(c => IsCtorPublicOrProtected(c))
        .OrderBy(c => c.ToString(), StringComparer.Ordinal)
        .ToArray();
    if (ctors.Length > 0)
    {
        lines.Add("### Constructors");
        lines.Add("");
        foreach (var ctor in ctors)
        {
            var parameters = string.Join(", ", ctor.GetParameters().Select(FormatParameter));
            var signature = $"{type.Name}({parameters})";
            var summary = xmlDocs.GetOrDefault(DocId.ForConstructor(ctor), $"Initializes a `{type.Name}` instance.");
            lines.Add($"- `{signature}`");
            lines.Add($"  - Summary: {summary}");
        }
        lines.Add("");
    }

    var properties = type.GetProperties(declared)
        .Where(p => IsPropertyPublicOrProtected(p))
        .OrderBy(p => p.Name, StringComparer.Ordinal)
        .ToArray();
    if (properties.Length > 0)
    {
        lines.Add("### Properties");
        lines.Add("");
        foreach (var property in properties)
        {
            var accessors = new List<string>(2);
            if (property.CanRead) accessors.Add("get");
            if (property.CanWrite) accessors.Add("set");
            var signature = $"{GetSimpleTypeName(property.PropertyType)} {property.Name} {{ {string.Join("; ", accessors)} }}";
            var summary = xmlDocs.GetOrDefault(DocId.ForProperty(property), $"Gets or sets `{property.Name}`.");
            lines.Add($"- `{signature}`");
            lines.Add($"  - Summary: {summary}");
        }
        lines.Add("");
    }

    var methods = type.GetMethods(declared)
        .Where(m => !m.IsSpecialName && IsMethodPublicOrProtected(m))
        .OrderBy(m => m.Name, StringComparer.Ordinal)
        .ThenBy(m => m.GetParameters().Length)
        .ToArray();
    if (methods.Length > 0)
    {
        lines.Add("### Methods");
        lines.Add("");
        foreach (var method in methods)
        {
            var parameters = string.Join(", ", method.GetParameters().Select(FormatParameter));
            var staticPrefix = method.IsStatic ? "static " : string.Empty;
            var signature = $"{staticPrefix}{GetSimpleTypeName(method.ReturnType)} {method.Name}({parameters})";
            var summary = xmlDocs.GetOrDefault(DocId.ForMethod(method), $"Invokes `{method.Name}`.");
            lines.Add($"- `{signature}`");
            lines.Add($"  - Summary: {summary}");
        }
        lines.Add("");
    }
}

Directory.CreateDirectory(Path.GetDirectoryName(outputPath)!);
File.WriteAllLines(outputPath, lines, new UTF8Encoding(encoderShouldEmitUTF8Identifier: false));
Console.WriteLine($"Generated API reference: {outputPath}");

if (verify)
{
    VerifyCoverage(types, lines);
}

return 0;

static string FormatParameter(ParameterInfo parameter)
{
    var modifier = parameter.IsOut ? "out " : parameter.ParameterType.IsByRef ? "ref " : string.Empty;
    var parameterType = parameter.ParameterType.IsByRef ? parameter.ParameterType.GetElementType()! : parameter.ParameterType;
    return $"{modifier}{GetSimpleTypeName(parameterType)} {parameter.Name}";
}

static bool IsCtorPublicOrProtected(ConstructorInfo ctor)
{
    return ctor.IsPublic || ctor.IsFamily || ctor.IsFamilyOrAssembly;
}

static bool IsMethodPublicOrProtected(MethodBase method)
{
    return method.IsPublic || method.IsFamily || method.IsFamilyOrAssembly;
}

static bool IsPropertyPublicOrProtected(PropertyInfo property)
{
    var getter = property.GetGetMethod(nonPublic: true);
    var setter = property.GetSetMethod(nonPublic: true);
    return (getter != null && IsMethodPublicOrProtected(getter)) || (setter != null && IsMethodPublicOrProtected(setter));
}

static string GetSimpleTypeName(Type type)
{
    if (type.IsGenericType)
    {
        var name = type.Name[..type.Name.IndexOf('`')];
        var args = string.Join(", ", type.GetGenericArguments().Select(GetSimpleTypeName));
        return $"{name}<{args}>";
    }

    if (type.IsArray)
    {
        return $"{GetSimpleTypeName(type.GetElementType()!)}[]";
    }

    return Type.GetTypeCode(type) switch
    {
        TypeCode.Boolean => "bool",
        TypeCode.Byte => "byte",
        TypeCode.SByte => "sbyte",
        TypeCode.Int16 => "short",
        TypeCode.UInt16 => "ushort",
        TypeCode.Int32 => "int",
        TypeCode.UInt32 => "uint",
        TypeCode.Int64 => "long",
        TypeCode.UInt64 => "ulong",
        TypeCode.Single => "float",
        TypeCode.Double => "double",
        TypeCode.Decimal => "decimal",
        TypeCode.String => "string",
        _ when type == typeof(void) => "void",
        _ when type == typeof(object) => "object",
        _ => type.Name
    };
}

static string GetPagmoReference(string typeName)
{
    var coreMap = new Dictionary<string, string>(StringComparer.Ordinal)
    {
        ["problem"] = "https://esa.github.io/pagmo2/docs/cpp/problem.html",
        ["algorithm"] = "https://esa.github.io/pagmo2/docs/cpp/algorithm.html",
        ["population"] = "https://esa.github.io/pagmo2/docs/cpp/population.html",
        ["island"] = "https://esa.github.io/pagmo2/docs/cpp/island.html",
        ["archipelago"] = "https://esa.github.io/pagmo2/docs/cpp/archipelago.html",
        ["bfe"] = "https://esa.github.io/pagmo2/docs/cpp/bfe.html",
        ["topology"] = "https://esa.github.io/pagmo2/docs/cpp/topology.html",
        ["r_policy"] = "https://esa.github.io/pagmo2/docs/cpp/r__policy.html",
        ["s_policy"] = "https://esa.github.io/pagmo2/docs/cpp/s__policy.html",
        ["pagmo"] = "https://esa.github.io/pagmo2/docs/cpp/utils/multi_objective.html",
    };
    if (coreMap.TryGetValue(typeName, out var coreUrl))
    {
        return coreUrl;
    }

    string[] problemTypes =
    [
        "ackley","cec2006","cec2009","cec2013","cec2014","decompose","dtlz","golomb_ruler","griewank",
        "hock_schittkowski_71","inventory","lennard_jones","luksan_vlcek1","minlp_rastrigin","null_problem",
        "rastrigin","rosenbrock","schwefel","translate","unconstrain","wfg","zdt"
    ];
    if (problemTypes.Contains(typeName, StringComparer.Ordinal))
    {
        return $"https://esa.github.io/pagmo2/docs/cpp/problems/{typeName}.html";
    }

    string[] algorithmTypes =
    [
        "bee_colony","cmaes","compass_search","cstrs_self_adaptive","de","de1220","gaco","gwo","ihs","ipopt","maco",
        "mbh","moead","moead_gen","nsga2","nspso","null_algorithm","pso","pso_gen","sade","sea","sga",
        "simulated_annealing","xnes","nlopt"
    ];
    if (algorithmTypes.Contains(typeName, StringComparer.Ordinal))
    {
        return $"https://esa.github.io/pagmo2/docs/cpp/algorithms/{typeName}.html";
    }

    string[] topologyTypes = ["unconnected", "fully_connected", "ring", "free_form"];
    if (topologyTypes.Contains(typeName, StringComparer.Ordinal))
    {
        return $"https://esa.github.io/pagmo2/docs/cpp/topologies/{typeName}.html";
    }

    string[] bfeTypes = ["default_bfe", "thread_bfe", "member_bfe"];
    if (bfeTypes.Contains(typeName, StringComparer.Ordinal))
    {
        return $"https://esa.github.io/pagmo2/docs/cpp/batch_evaluators/{typeName}.html";
    }

    return "https://esa.github.io/pagmo2/docs/cpp/cpp_docs.html";
}

static void VerifyCoverage(Type[] types, List<string> lines)
{
    const BindingFlags declared = BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Instance | BindingFlags.Static | BindingFlags.DeclaredOnly;

    int expectedTypeSummaries = types.Length;
    int expectedMemberSummaries = 0;
    foreach (var type in types)
    {
        if (type.IsEnum)
        {
            continue;
        }

        var ctorCount = type.GetConstructors(declared).Count(IsCtorPublicOrProtected);
        var propertyCount = type.GetProperties(declared).Count(IsPropertyPublicOrProtected);
        var methodCount = type.GetMethods(declared).Count(m => !m.IsSpecialName && IsMethodPublicOrProtected(m));
        expectedMemberSummaries += ctorCount + propertyCount + methodCount;
    }

    int actualTypeSummaries = lines.Count(l => l.StartsWith("- Summary: ", StringComparison.Ordinal));
    int actualMemberSummaries = lines.Count(l => l.StartsWith("  - Summary: ", StringComparison.Ordinal));

    if (actualTypeSummaries < expectedTypeSummaries)
    {
        throw new InvalidOperationException($"API docs missing type summaries: expected at least {expectedTypeSummaries}, found {actualTypeSummaries}.");
    }

    if (actualMemberSummaries < expectedMemberSummaries)
    {
        throw new InvalidOperationException($"API docs missing member summaries: expected at least {expectedMemberSummaries}, found {actualMemberSummaries}.");
    }

    Console.WriteLine($"API docs coverage verified. Type summaries: {actualTypeSummaries}/{expectedTypeSummaries}; member summaries: {actualMemberSummaries}/{expectedMemberSummaries}");
}

internal static class DocId
{
    public static string ForType(Type type) => $"T:{NormalizeType(type)}";

    public static string ForProperty(PropertyInfo property)
    {
        return $"P:{NormalizeType(property.DeclaringType!)}.{property.Name}";
    }

    public static string ForMethod(MethodInfo method)
    {
        var prefix = $"M:{NormalizeType(method.DeclaringType!)}.{method.Name}";
        return AppendParameters(prefix, method.GetParameters());
    }

    public static string ForConstructor(ConstructorInfo ctor)
    {
        var prefix = $"M:{NormalizeType(ctor.DeclaringType!)}.#ctor";
        return AppendParameters(prefix, ctor.GetParameters());
    }

    private static string AppendParameters(string prefix, ParameterInfo[] parameters)
    {
        if (parameters.Length == 0)
        {
            return prefix;
        }

        var parameterList = string.Join(",", parameters.Select(p => NormalizeParameterType(p.ParameterType)));
        return $"{prefix}({parameterList})";
    }

    private static string NormalizeParameterType(Type type)
    {
        if (type.IsByRef)
        {
            return $"{NormalizeParameterType(type.GetElementType()!)}@";
        }

        if (type.IsArray)
        {
            return $"{NormalizeParameterType(type.GetElementType()!)}[]";
        }

        if (type.IsGenericType)
        {
            var genericTypeDefName = type.GetGenericTypeDefinition().FullName!;
            var tickIndex = genericTypeDefName.IndexOf('`');
            if (tickIndex >= 0)
            {
                genericTypeDefName = genericTypeDefName.Substring(0, tickIndex);
            }

            var args = string.Join(",", type.GetGenericArguments().Select(NormalizeParameterType));
            return $"{genericTypeDefName}{{{args}}}";
        }

        return NormalizeType(type);
    }

    private static string NormalizeType(Type type)
    {
        return type.FullName ?? type.Name;
    }
}

internal sealed class XmlDocs
{
    private readonly Dictionary<string, string> _summaries;

    private XmlDocs(Dictionary<string, string> summaries)
    {
        _summaries = summaries;
    }

    public static XmlDocs Load(string xmlPath)
    {
        if (!File.Exists(xmlPath))
        {
            return new XmlDocs(new Dictionary<string, string>(StringComparer.Ordinal));
        }

        var doc = XDocument.Load(xmlPath);
        var summaries = new Dictionary<string, string>(StringComparer.Ordinal);
        var members = doc.Root?.Element("members")?.Elements("member") ?? Enumerable.Empty<XElement>();
        foreach (var member in members)
        {
            var name = member.Attribute("name")?.Value;
            if (string.IsNullOrWhiteSpace(name))
            {
                continue;
            }

            var summaryText = member.Element("summary")?.Value;
            if (string.IsNullOrWhiteSpace(summaryText))
            {
                continue;
            }

            var normalized = NormalizeWhitespace(summaryText);
            if (normalized.Length > 0)
            {
                summaries[name] = normalized;
            }
        }

        return new XmlDocs(summaries);
    }

    public string GetOrDefault(string docId, string fallback)
    {
        return _summaries.TryGetValue(docId, out var summary) ? summary : fallback;
    }

    private static string NormalizeWhitespace(string value)
    {
        return string.Join(" ", value.Split((char[]?)null, StringSplitOptions.RemoveEmptyEntries));
    }
}
